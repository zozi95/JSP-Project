package user;

public class User {

	private String userID;
	private String guestPassword;
	private String guestName;
	private String guestGender;
	private String guestMail;
	private int	guestPhone;
	private String guestIntroduction;
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getGuestPassword() {
		return guestPassword;
	}
	public void setGuestPassword(String guestPassword) {
		this.guestPassword = guestPassword;
	}
	public String getGuestName() {
		return guestName;
	}
	public void setGuestName(String guestName) {
		this.guestName = guestName;
	}
	public String getGuestGender() {
		return guestGender;
	}
	public void setGuestGender(String guestGender) {
		this.guestGender = guestGender;
	}
	public String getGuestMail() {
		return guestMail;
	}
	public void setGuestMail(String guestMail) {
		this.guestMail = guestMail;
	}
	public int getGuestPhone() {
		return guestPhone;
	}
	public void setGuestPhone(int guestPhone) {
		this.guestPhone = guestPhone;
	}
	public String getGuestIntroduction() {
		return guestIntroduction;
	}
	public void setGuestIntroduction(String guestIntroduction) {
		this.guestIntroduction = guestIntroduction;
	}
	public String getGuestFilename() {
		return guestFilename;
	}
	public void setGuestFilename(String guestFilename) {
		this.guestFilename = guestFilename;
	}
	public String getUsertype() {
		return usertype;
	}
	public void setUsertype(String usertype) {
		this.usertype = usertype;
	}
	private String guestFilename;
	private String usertype;
}
	