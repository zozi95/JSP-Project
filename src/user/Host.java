package user;

public class Host {

	private String userID;
	private String hostName;

	private String hostCountry;
	private String hostWork;
	private String hostoneIntroduction;
	private String hostIntroduction;
	private String hostFilename;
	private String hosttype;
	
	
	public String getHostName() {
		return hostName;
	}
	public void setHostName(String hostName) {
		this.hostName = hostName;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getHostCountry() {
		return hostCountry;
	}
	public void setHostCountry(String hostCountry) {
		this.hostCountry = hostCountry;
	}
	public String getHostWork() {
		return hostWork;
	}
	public void setHostWork(String hostWork) {
		this.hostWork = hostWork;
	}
	public String getHostoneIntroduction() {
		return hostoneIntroduction;
	}
	public void setHostoneIntroduction(String hostoneIntroduction) {
		this.hostoneIntroduction = hostoneIntroduction;
	}
	public String getHostIntroduction() {
		return hostIntroduction;
	}
	public void setHostIntroduction(String hostIntroduction) {
		this.hostIntroduction = hostIntroduction;
	}
	public String getHostFilename() {
		return hostFilename;
	}
	public void setHostFilename(String hostFilename) {
		this.hostFilename = hostFilename;
	}
	public String getHosttype() {
		return hosttype;
	}
	public void setHosttype(String hosttype) {
		this.hosttype = hosttype;
	}
	
}
	